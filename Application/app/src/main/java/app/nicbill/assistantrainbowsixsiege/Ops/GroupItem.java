package app.nicbill.assistantrainbowsixsiege.Ops;

public class GroupItem {

    private String idImageGI;
    private String nomGI;

    public GroupItem(String idImageGI, String nomGI) {
        this.idImageGI = idImageGI;
        this.nomGI = nomGI;
    }

    public String getIdImageGI() {
        return idImageGI;
    }

    public String getNomGI() {
        return nomGI;
    }
}
